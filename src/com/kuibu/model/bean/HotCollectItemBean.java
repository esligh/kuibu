package com.kuibu.model.bean;


public class HotCollectItemBean {
	String   folder_name;
	String	 folder_desc;
	String   folder_owner_pic_id;
	String   folder_owner_name;
	String   folder_visit_count;
	
	public String getFolder_name() {
		return folder_name;
	}
	public void setFolder_name(String folder_name) {
		this.folder_name = folder_name;
	}
	public String getFolder_desc() {
		return folder_desc;
	}
	public void setFolder_desc(String folder_desc) {
		this.folder_desc = folder_desc;
	}
	public String getFolder_owner_pic_id() {
		return folder_owner_pic_id;
	}
	public void setFolder_owner_pic_id(String folder_owner_pic_id) {
		this.folder_owner_pic_id = folder_owner_pic_id;
	}
	public String getFolder_owner_name() {
		return folder_owner_name;
	}
	public void setFolder_owner_name(String folder_owner_name) {
		this.folder_owner_name = folder_owner_name;
	}
	public String getFolder_visit_count() {
		return folder_visit_count;
	}
	public void setFolder_visit_count(String folder_visit_count) {
		this.folder_visit_count = folder_visit_count;
	}	
}
