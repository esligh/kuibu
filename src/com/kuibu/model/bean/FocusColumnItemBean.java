package com.kuibu.model.bean;

public class FocusColumnItemBean {
	private String column_name;
	private String column_introduce;
	private String collect_count;
	private String owner_pic;
	public String getColumn_name() {
		return column_name;
	}
	public void setColumn_name(String column_name) {
		this.column_name = column_name;
	}
	public String getColumn_introduce() {
		return column_introduce;
	}
	public void setColumn_introduce(String column_introduce) {
		this.column_introduce = column_introduce;
	}
	public String getCollect_count() {
		return collect_count;
	}
	public void setCollect_count(String collect_count) {
		this.collect_count = collect_count;
	}
	public String getOwner_pic() {
		return owner_pic;
	}
	public void setOwner_pic(String owner_pic) {
		this.owner_pic = owner_pic;
	}
	
}
